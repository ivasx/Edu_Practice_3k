week = {
    1: 'Понеділок',
    2: 'Вівторок',
    3: 'Середа',
    4: 'Четвер',
    5: 'П\'ятниця',
    6: 'Субота',
    7: 'Неділя'
}

while True:
    num = input('Введіть порядковий номер дня тижня: ')
    try:
        num = int(num)
        if num in week:
            print(week[num])
            break
        else:
            print('Введений вами номер не входить в діапазон тижня.')
    except ValueError:
        print('Введене вами значення не є номером')